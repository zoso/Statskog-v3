                            <h2>Administrerende direktør har ordet</h2>
                            <div class="border-down">&nbsp;</div>
                            <span class="ingress">For Statskog ble 2012 nok et år der foretakets resultat er preget av en robust underliggende drift og betydelige verneinntekter og inntekter fra salg av eiendom. Statskogs strategi følges, de fem regionene har ansvaret for god drift innenfor eiendom og jakt, fiske og annet friluftsliv og forretningsområdene skog og energi leverer i tråd med forventningene.</span>
                            <p>&nbsp;</p><p>Skogsdrifta gikk etter planen. Til tross for litt synkende priser mot slutten av året, har jevn drift gjennom hele året sikret inntjening i tråd med forventningene. Vi er også godt i gang med å utvikle et planleggingsverktøy i samarbeid med professor emeritus Kåre Hobbelstad og Geodata. Verktøyet som vil være unikt i norsk sammenheng, blir klart til bruk i 2013. Det vil gi Statskog oversikt over utviklingen av våre skogressurser i et hundreårsperspektiv, og sikre en langsiktig og bærekraftig forvaltning av skogen. Dette arbeidet er et godt eksempel på at Statskog er seg sitt ansvar bevisst som stor statlig grunneier, og tar initiativ som vil komme hele bransjen til nytte. Som landets mest profesjonelle grunneier skal vi vise veg.</p>
                            <p>Det største salget av statlig grunn i moderne tid er i rute. I forbindelse med at Statskog kjøpte Orklas skogeiendommer i Hedmark og Buskerud i desember 2010, ble det bestemt at Statskog skulle selge spredte skogeiendommer foretaket eide fra før. I løpet av 2011 og 2012 har Statskog solgt til sammen 92 eiendommer i 61 kommuner i 13 fylker – for en samlet sum på om lag 150 millioner kroner. Salget fortsetter i 2013 med to utlysingsrunder – en på våren og en til høsten. Salget vil bli avsluttet i 2017.</p>
                            <p>Til sammen skal salget utgjøre rundt 500 000 dekar fordelt på om lag 230 eiendommer. Hittil er 130 000 dekar solgt. I 2013 vil 125 000 dekar bli solgt, fordelt på ca. 40 eiendommer i 13 fylker. Statskog vil ved utgangen av 2013 være ferdig med om lag halvparten av det totale salget.</p>
                            <p>Salget vil gi Statskog bedre arronderte eiendommer, som igjen vil gi et enda bedre grunnlag for effektiv drift. I tillegg får lokale grunneiere muligheten til å styrke sin drift gjennom kjøp av tilliggende grunn. En stor andel av de solgte eiendommene har da også fått lokale kjøpere. Det er i tråd med den politiske intensjonen, og svært gledelig. 
                            </p><p>Innenfor energiområdet begynner nå Statskog å høste gevinster av systematisk arbeid over flere år. I 2012 startet byggingen av det første småkraftverket Statskog er medeier i. Planlagt produksjonsstart er høsten 2013.  Statskog har nå fått fire vannkraftkonsesjoner og har om lag 40 konsesjonssøknader til behandling hos NVE. I 2012 fikk vi også den første konsesjonen for vindkraftproduksjon. På energiområdet samarbeider Statskog med både energiselskaper og med private grunneiere.</p>
                            <p>Som fellesskapets grunneier er det viktig for Statskog fortsatt å være en garantist for allmennhetens tilgang til jakt og fiske. Vår forvaltning skal, også på jakt- og fiskeområdet, være kunnskapsbasert. Derfor engasjerer vi oss i forskning. Samtidig er vi opptatt av å ha en god dialog med relevante aktører, som for eksempel Norges Jeger og Fiskerforbund. Enhver som følger debatten om rypejakta i mediene vet at mange mener mye forskjellig om hvordan en best skal forvalte bestanden. Rypebestanden synker mens antallet jegere øker. Statskog er opptatt av i dialog med omverdenen å gjennomføre en forvaltning som ikke truer rypebestanden.</p>
                            <p>Statskog har mange spennende utfordringer framover. For å bli enda bedre til å løse de oppgaver foretaket er satt til, satser vi mye på utvikling og deling av organisasjonens og den enkelte ansattes kompetanse. Jeg opplever at vi har en svært utviklingsorientert organisasjon med en imponerende omstillingsevne. Jeg føler meg derfor trygg på at Statskog også i tiden som kommer vil levere gode resultater til fellesskapets beste.</p>
                            <p><i>Øistein Aagesen, administrerende direktør</i></p>
