                            <h2>Revisors beretning</h2>
                            <div class="border-down">&nbsp;</div>
                            <p><b>Uttalelse om årsregnskapet</b><br>
                            Vi har revidert årsregnskapet for Statskog SF, som består av selskapsregnskap, som viser et
                            overskudd på kr 137 710 000, og konsernregnskap, som viser et overskudd på kr 118 022 000.
                            Selskapsregnskapet og konsernregnskapet består av balanse per 31. desember 2012,
                            resultatregnskap og kontantstrømoppstilling for regnskapsåret avsluttet per denne datoen og en
                            beskrivelse av vesentlige anvendte regnskapsprinsipper og andre noteopplysninger.</p>
                            <p><i>Styrets og administrerende direktørs ansvar for årsregnskapet</i><br>
                            Styret og administrerende direktør er ansvarlig for å utarbeide årsregnskapet og for at det giret
                            rettvisende bilde i samsvar med regnskapslovens regler og god regnskapsskikk i Norge, og for slik
                            intern kontroll som styret og administrerende direktør finner nødvendig for å muliggjøre utarbeidelsen av et årsregnskap som ikke inneholder vesentlig feilinformasjon, verken som følge av misligheter eller feil.<p>
                            <p><i>Revisors oppgaver og plikter</i><br>
                            Vår oppgave er å gi uttrykk foren mening om dette årsregnskapet på bakgrunn av vår revisjon. Vi har
                            gjennomført revisjonen i samsvar med lov, forskrift og god revisjonsskikk i Norge, herunder
                            International Standards on Auditing. Revisjonsstandardene krever at vi etterlever etiske krav og
                            planlegger og gjennomfører revisjonen for å oppnå betryggende sikkerhet for at årsregnskapet ikke
                            inneholder vesentlig feilinformasjon.</p>
                            <p>En revisjon innebærer utførelse av handlinger for å innhente revisjonsbevis for beløpene og
                            opplysningene i årsregnskapet. De valgte handlingene avhenger av revisors skjønn, herunder
                            vurderingen av risikoene for at årsregnskapet inneholder vesentlig feilinformasjon, enten det skyldes
                            misligheter eller feil. Ved en slik risikovurdering tar revisor hensyn til den interne kontrollen som er relevant for selskapets utarbeidelse av et årsregnskap som giret rettvisende bilde. Formålet er å utforme revisjonshandlinger som er hensiktsmessige etter omstendighetene, men ikke for å gi uttrykk foren mening om effektiviteten av selskapets interne kontroll. En revisjon omfatter også en vurdering av om de anvendte regnskapsprinsippene er hensiktsmessige og om regnskapsestimatene utarbeidet av ledelsen er rimelige, samt en vurdering av den samlede presentasjonen av årsregnskapet.</p><p>Etter vår oppfatning er innhentet revisjonsbevis tilstrekkelig og hensiktsmessig som grunnlag for vår konklusjon om selskapsregnskapet og vår konklusjon om konsernregnskapet.</p>
                            <p><i>Konklusjon</i><br>
                            Etter vår mening er årsregnskapet for Statskog SF avgitt i samsvar med lov og forskrifter og giret
                            rettvisende bilde av selskapets og konsernets finansielle stilling per 31. desember 2012 og av deres
                            resultater og kontantstrømmer for regnskapsåret som ble avsluttet per denne datoen i samsvar med
                            regnskapslovens regler og god regnskapsskikk i Norge.
                            </p>
                            <p><b>Uttalelse om øvrige forhold</b></p>
                            <p><i>Konklusjon om årsberetningen</i><br>
                            Basert på vår revisjon av årsregnskapet som beskrevet ovenfor, mener vi at opplysningene i
                            årsberetningen om årsregnskapet, forutsetningen om fortsatt drift og forslaget til disponering av
                            resultatet er konsistente med årsregnskapet og i samsvar med lov og forskrifter.</p>
                            <p><i>Konklusjon om registrering og dokumentasjon</i><br>
                            Basert på vår revisjon av årsregnskapet som beskrevet ovenfor, og kontrollhandlinger vi har funnet
                            nødvendig i henhold til internasjonal standard for attestasjonsoppdrag (ISAE) 3000 «Attestasjonsoppdrag som ikke er revisjon eller forenklet revisorkontroll av historisk finansiell
                            informasjon», mener vi at styret og administrerende direktør har oppfylt sin plikt til å sørge for ordentlig og oversiktlig registrering og dokumentasjon av selskapets regnskapsopplysninger isamsvar med lov og god bokføringsskikk i Norge.</p>
